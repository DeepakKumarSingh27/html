package com.company;

public class cwh_22_ch5_dowhileloop {
    public static void main(String[] args) {
        int b=0;
        do{
            System.out.println(b);
            b++;
        }while(b<5);
        int n=1;
        do{
            System.out.println(n);
            n++;
        }while(n<=100);
    }
}
